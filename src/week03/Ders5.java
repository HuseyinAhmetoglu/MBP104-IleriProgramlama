package week03;

public class Ders5 {

	public static void main(String[] args) {

		double sayi = 4.0 / 3.0;
		System.out.println(sayi);

		System.out.printf("%.3f", sayi);

		int sayi2 = 123;

		System.out.println();

		System.out.printf("%05d", sayi2);
		System.out.println();

		System.out.printf("%8d", sayi2);

		int sayi3 = 15;
		System.out.println();

		System.out.printf("%x", sayi3);
		System.out.println();

		System.out.printf("%X", sayi3);

	}

}
